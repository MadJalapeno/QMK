// Draw to OLED
bool oled_task_user() {

    if (!is_oled_enabled) {
        oled_off();
        return false;
    } else {
        oled_on();
    }

    if (is_keyboard_master()) {
        oled_render_layer_state();
        oled_render_keylog();
    } else {
        oled_render_logo();
        oled_scroll_left();
    }
    return true;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
    set_keylog(keycode, record);
    oled_timer_reset();

  }
  return true;
}